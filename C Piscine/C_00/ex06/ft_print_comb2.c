#include <unistd.h>

void	int_write(int n)
{
	write(1, &"0123456789"[n / 10], 1);
	write(1, &"0123456789"[n % 10], 1);
}

void	ft_print_comb2(void)
{
	int	a;
	int	b;

	a = -1;
	while (++a <= 98)
	{
		b = a;
		while (++b <= 99)
		{
			int_write(a);
			write(1, " ", 1);
			int_write(b);
			if ((a != 98) || (b != 99))
			{
				write(1, ", ", 2);
			}
		}
	}
}
