int	calc_sum(int a, int b)
{
	return (a + b);
}

int	calc_sub(int a, int b)
{
	return (a - b);
}

int	calc_mul(int a, int b)
{
	return (a * b);
}

int	calc_div(int a, int b)
{
	return (a / b);
}

int	calc_mod(int a, int b)
{
	return (a % b);
}
