#include <stdlib.h>

int	ft_strlen(char *str)
{
	int	cnt;

	cnt = 0;
	while (str[cnt] != '\0')
		cnt++;
	return (cnt);
}

char	*ft_strcpy(char *dest, char *src)
{
	int	i;

	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

char	*ft_strdup(char *str)
{
	char	*pc;
	int		len;

	len = ft_strlen(str);
	pc = (char *)malloc(sizeof(char) * (len + 1));
	if (pc != NULL)
		ft_strcpy(pc, str);
	return (pc);
}
